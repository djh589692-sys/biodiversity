def render_funfacts():
    st.title("💡 科普趣味卡片")

    tab1, tab2 = st.tabs(["🌟 你知道吗", "🧠 小测验"])

    with tab1:
        # 合并了冷知识和小知识
        all_knowledge = FACTS + DYK
        if st.button("换一批", key="shuffle_facts"):
            st.session_state["fact_offset"] = (
                st.session_state.get("fact_offset", 0) + 6
            ) % len(all_knowledge)

        offset = st.session_state.get("fact_offset", 0)
        selected = [all_knowledge[(offset + i) % len(all_knowledge)] for i in range(6)]

        for item in selected:
            with st.container(border=True):
                if isinstance(item, tuple) and len(item) == 2:
                    title, content = item
                    st.markdown(f"**💡 {title}**")
                    st.markdown(content)
                else:
                    cat, text = item
                    st.markdown(f"**[{cat}]**")
                    st.markdown(text)

    with tab2:
        # 小测验 - 每次随机抽取5题
        if "quiz_pool" not in st.session_state or st.session_state.get("quiz_pool", []) == []:
            st.session_state.quiz_pool = random.sample(ALL_QUIZ, min(5, len(ALL_QUIZ)))
        
        if "quiz_idx" not in st.session_state:
            st.session_state.quiz_idx = 0
            st.session_state.quiz_score = 0
            st.session_state.quiz_answered = False
            st.session_state.quiz_choice = None

        idx = st.session_state.quiz_idx
        score = st.session_state.quiz_score
        quiz_pool = st.session_state.quiz_pool

        if idx >= len(quiz_pool):
            pct = score / len(quiz_pool)
            if pct == 1.0:
                emoji, msg = "🏆", "满分！生物多样性达人"
            elif pct >= 0.6:
                emoji, msg = "🌱", "不错！继续探索自然"
            else:
                emoji, msg = "📚", "还需多了解大自然"
            st.success(f"{emoji} {msg}")
            st.metric("最终得分", f"{score} / {len(quiz_pool)}")
            
            col1, col2 = st.columns(2)
            with col1:
                if st.button("🔄 再来一次（新题目）", use_container_width=True):
                    # 重置并抽取新题目
                    st.session_state.quiz_pool = random.sample(ALL_QUIZ, min(5, len(ALL_QUIZ)))
                    st.session_state.quiz_idx = 0
                    st.session_state.quiz_score = 0
                    st.session_state.quiz_answered = False
                    st.session_state.quiz_choice = None
                    st.rerun()
            with col2:
                if st.button("🔁 重玩本轮", use_container_width=True):
                    # 复用同一套题目
                    st.session_state.quiz_idx = 0
                    st.session_state.quiz_score = 0
                    st.session_state.quiz_answered = False
                    st.session_state.quiz_choice = None
                    st.rerun()
        else:
            q = quiz_pool[idx]
            st.markdown(f"**第 {idx+1} 题 / 共 {len(quiz_pool)} 题**")
            st.progress(idx / len(quiz_pool))
            st.markdown(f"#### {q['q']}")

            choice = st.radio(
                "选择答案",
                [f"{chr(65+i)}. {o}" for i, o in enumerate(q["opts"])],
                key=f"quiz_radio_{idx}",
                label_visibility="collapsed",
                disabled=st.session_state.quiz_answered,
            )

            if not st.session_state.quiz_answered:
                if st.button("确认答案", key=f"confirm_{idx}"):
                    chosen_idx = ord(choice[0]) - 65
                    st.session_state.quiz_answered = True
                    st.session_state.quiz_choice = chosen_idx
                    if chosen_idx == q["ans"]:
                        st.session_state.quiz_score += 1
                    st.rerun()
            else:
                chosen = st.session_state.quiz_choice
                if chosen == q["ans"]:
                    st.success("✅ 回答正确！")
                else:
                    correct_letter = chr(65 + q["ans"])
                    st.error(f"❌ 答错了！正确答案是 {correct_letter}. {q['opts'][q['ans']]}")
                with st.expander("查看解析"):
                    st.markdown(q["exp"])
                if st.button("下一题", key=f"next_{idx}"):
                    st.session_state.quiz_idx += 1
                    st.session_state.quiz_answered = False
                    st.session_state.quiz_choice = None
                    st.rerun()
